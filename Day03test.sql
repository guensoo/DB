-- 7.
SELECT EMPLOYEE_ID, FIRST_NAME, SALARY
FROM EMPLOYEES
WHERE LENGTH(FIRST_NAME) >= 6;
-- 8.
SELECT EMPLOYEE_ID,
MOD(EMPLOYEE_ID, 2)
FROM EMPLOYEES;
-- 9.
